x = 5
y = 10

# karena x lebih kecil dari y adalah benar, maka kode didalam scope satesen if akan dijalankan
if (x < y):
    print("Nilai x lebih kecil dari y ")

# karena x lebih besar dari y adalah salah, maka kode didalam scope satesen if tidak akan dijalankan
if (x > y):
    print("Nilai x lebih besar dari y ")